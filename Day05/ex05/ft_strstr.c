char	*ft_strstr(char *str, char *to_find)
{
	char *t1;
	char *t2;

	if (*to_find == 0)
		return (0);
	while (*str != 0)
	{
		t1 = str;
		t2 = to_find;
		while (*t1 == *t2 && *t1 != 0 && *t2 != 0)
		{
			t1++;
			t2++;
		}
		if (*t2 == 0)
			return (str);
		str++;
	}
	return (0);
}
